let a = +prompt("Введите число от 1 до 5:");
switch(a) {
    case 1:
        console.log("Один");
        break;
    case 2:
        console.log("Два");
        break;
    case 3:
        console.log("Три");
        break;
    case 4:
        console.log("Четыре");
        break;
    case 5:
        console.log("Пять");
        break;
    default:
        console.log("Мы таких чисел не знаем");
}